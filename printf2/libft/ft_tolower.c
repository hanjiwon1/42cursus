#include "libft.h"

int	ft_tolower(int c)
{
	int	dip;

	dip = 'a' - 'A';
	if (!ft_isalpha(c))
		return (c);
	if (c >= 'A' && c <= 'Z')
		c += dip;
	return (c);
}
