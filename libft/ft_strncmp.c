#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t idx;

	idx = 0;
	while (idx < n)
	{
		if (*s1 != *s2 || !(*s1))
			return (*s1 - *s2);
		s1++;
		s2++;
		idx++;
	}
	return (0);
}
